package com.tns.day3;

public class ForDemo {

	public static void main(String[] args) {
		for(int i=1;i<=5;i++)
			System.out.println("hello");
		
		
		for(int j=1;j<=20;j++)
			System.out.println(j);
		
		
		for(int k=100;k<=1;k--)
			System.out.println(k);
		
		for(int l=10;l>=1;l--)
			System.out.println(l);

	}

}
